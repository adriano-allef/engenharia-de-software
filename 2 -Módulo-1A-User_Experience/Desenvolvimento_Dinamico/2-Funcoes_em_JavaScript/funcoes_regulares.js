//Definindo outra função regular com dois parâmetros
function soma(a, b) {
    return a + b;
}

// Chamando a função soma e armazenando o resultado em uma variável
let resultado = soma(5, 3);

// Exibindo o resultado da soma no console
console.log("O resultado da soma é: ", resultado);

//Definindo uma função regular usando palavra-chave function
function saudacao(nome) {
    console.log("Olá, " + nome + "!");
}

// Chamando a função saudacao com um argumento
saudacao("Adriano Allef");

// Definindo uma função regular sem parametro e sem retorno
function mensagem() {
    console.log("Esta é uma mensagem sem parâmetros e sem retorno.");
}

// Chamando a função mensagem
saudacao("Adriano Allef");
mensagem();