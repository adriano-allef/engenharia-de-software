// Definindo uma string de exemplo
let nome = "Ana Teixeira";

// Usando o método tolowerCase() para converter a string para minúsculas
let nomeMinusculo = nome.toLowerCase();
console.log("Nome em minúsculas:", nomeMinusculo);

// Usando o método toUpperCase() para converter a string para maiúsculas
let nomeMaiusculo = nome.toUpperCase();
console.log("Nome em maiúsculas:", nomeMaiusculo);

