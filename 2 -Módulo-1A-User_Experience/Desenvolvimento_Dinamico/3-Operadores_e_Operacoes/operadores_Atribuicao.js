// Operador de atribuição básico (=)
let a = 10; // Atribui o valor 10 à variável a
console.log(`Valor inicial de a: ${a}`); // Exibe o valor de a

// Operador de atribuição com adição (+=)
a += 5; // Adiciona 5 ao valor atual de a
console.log(` Após a += 5, valor de a: ${a}`);

// Operador de atribuição com subtração (-=)
a -= 3; // a = a - 3
console.log(` Após a -= 3, valor de a: ${a}`);

// Operador de atribuição com multiplicação (*=)
a *= 2; // a = a * 2
console.log(` Após a *= 2, valor de a: ${a}`);

// Operador de atribuição com divisão (/=)
a /= 4; // a = a / 4
console.log(` Após a /= 4, valor de a: ${a}`);

// Operador de atribuição com módulo (%=)
a %= 4; // a = a % 4
console.log(` Após a %= 3, valor de a: ${a}`);

// Operador de atribuição de resto (%)
a = a % 2
console.log(` Após a %= 2, valor de a: ${a}`);

// Operador de atribuição com exponenciação (**=)
a **= 2; // a = a ** 2
console.log(` Após a **= 2, valor de a: ${a}`);